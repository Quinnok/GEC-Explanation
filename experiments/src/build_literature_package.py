def write_xlsx() -> None:
    try:
        from openpyxl import Workbook
        from openpyxl.styles import Font, PatternFill
        from openpyxl.utils import get_column_letter
    except ImportError as exc:  # pragma: no cover - dependency is installed in Round 05
        raise SystemExit("openpyxl is required for literature_matrix.xlsx") from exc

    wb = Workbook()
    ws = wb.active
    ws.title = "Literature Matrix"
    ws.append(MATRIX_COLUMNS)
    for paper in PAPERS:
        row = matrix_row(paper)
        ws.append([row[col] for col in MATRIX_COLUMNS])
    for cell in ws[1]:
        cell.font = Font(bold=True, color="FFFFFF")
        cell.fill = PatternFill("solid", fgColor="4F81BD")
    widths = {
        "A": 12,
        "B": 8,
        "C": 54,
        "D": 24,
        "E": 26,
        "F": 28,
    }
    for idx in range(1, len(MATRIX_COLUMNS) + 1):
        letter = get_column_letter(idx)
        ws.column_dimensions[letter].width = widths.get(letter, 38)
    ws.freeze_panes = "A2"
    ws.auto_filter.ref = ws.dimensions
    wb.save(OUT / "literature_matrix.xlsx")


