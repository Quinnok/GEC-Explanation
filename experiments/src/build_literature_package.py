    path = CARD_DIR / filename
    fields = [
        ("Title", paper.title),
        ("Authors", paper.authors),
        ("Year", str(paper.year)),
        ("Venue", paper.venue),
        ("Formal publication status", paper.status),
        ("Official link", paper.link),
        ("Code and data links", paper.code_data),
        ("Research background", paper.background),
        ("Paper entry point", paper.entry_point),
        ("Research question", paper.task),
        ("Core hypothesis", paper.core_idea),
        ("Core idea", paper.core_idea),
        ("Innovation", paper.innovation),
        ("Detailed method", paper.detailed_method),
        ("Model input/output", paper.io),
        ("Mathematical objective", paper.mathematical_objective),
        ("Data construction", paper.data_construction),
        ("Datasets", paper.data),
        ("Baselines", paper.baselines),
        ("Metrics", paper.metrics),
        ("Main experiments", paper.main_experiments),
        ("Ablation experiments", paper.ablations),
        ("Analysis experiments", paper.findings),
        ("Human evaluation", paper.human_evaluation),
        ("How experiments support claims", paper.evidence_for_claims),
        ("Main results", paper.findings),
        ("Limitations", paper.limitations),
        ("Unsolved problems", paper.new_gap),
        ("Relation to current research", paper.relation_to_us),
        ("Threat to current novelty", paper.novelty_threat),
        ("Reusable modules", paper.reusable_module),
        ("Extensible research gap", paper.new_gap),
        ("Reading priority", paper.priority),
        ("Round 05 verification note", "Metadata and abstracts were checked from official pages on 2026-07-18; detailed PDF-level claims still need a final citation pass before submission."),
    ]
    body = [f"# {paper.title}", ""]
    for name, value in fields:
        body.append(f"## {name}")
        body.append(wrap(value))
        body.append("")
    path.write_text("\n".join(body), encoding="utf-8")


