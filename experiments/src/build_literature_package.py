def slugify(text: str) -> str:
    text = text.lower()
    text = re.sub(r"[^a-z0-9]+", "-", text)
    return text.strip("-")[:80]


def wrap(value: str) -> str:
    return "\n".join(textwrap.wrap(value, width=96)) if value else "Not recorded"


